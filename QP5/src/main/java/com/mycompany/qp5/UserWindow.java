    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int selectedIndex = jListRoomInventory.getSelectedIndex();
        if (selectedIndex != -1) {
            CustomListModel roomItems = ((CustomListModel)jListRoomInventory.getModel());
            CustomListModel myItems = ((CustomListModel)jListMyInventory.getModel());
            myItems.addElement(roomItems.removeElement(selectedIndex));
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDropActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDropActionPerformed
        int selected = jListMyInventory.getSelectedIndex();
        if (selected != -1) {
            CustomListModel myItems = ((CustomListModel)jListMyInventory.getModel());
            CustomListModel roomItems = ((CustomListModel)jListRoomInventory.getModel());
            if(myItems.getSize() > 0) {
                roomItems.addElement(myItems.removeElement(selected));
            }
        }
    }//GEN-LAST:event_btnDropActionPerformed

    private void btnInspectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInspectActionPerformed
        int selectedIndex = jListMyInventory.getSelectedIndex();
        if (selectedIndex != -1) {
            CustomListModel myItems = ((CustomListModel)jListMyInventory.getModel());
            String description = myItems.getElementAtDesc(selectedIndex);
            JOptionPane.showMessageDialog(null, description, "Description", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnInspectActionPerformed

