    private class CustomListModel extends AbstractListModel<String> {
        
        private ArrayList<GameItem> items = new ArrayList<GameItem>();
        
        public CustomListModel() {
            super();
        }
        
        @Override
        public int getSize() {
            return items.size();
        }
        
        @Override
        //Gets Item Name
        public String getElementAt(int index) { 
            return items.get(index).getName();
        }
        
        //Gets item Description
        public String getElementAtDesc(int index) {
            return items.get(index).getDescription();
        }
        
        public void addElement(GameItem item){
            items.add(item);
            int t = items.size()-1;
            fireContentsChanged(this, t, t);
        }
        
        public GameItem removeElement(int index){
            if(items.size() > 0){
                GameItem item = items.remove(index);
                fireContentsChanged(this, index, items.size()-1); 
                return item;
            }
            return null;
        }
    }
    
